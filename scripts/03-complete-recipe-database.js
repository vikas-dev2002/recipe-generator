async function seedCompleteDatabase() {
  const client = new MongoClient(MONGO_URI)

  try {
    await client.connect()
    const db = client.db("RecipeDB")

    // Clear existing recipes
    //await db.collection("recipes").deleteMany({})

    // Insert complete recipe collection
    const result = await db.collection("recipes").insertMany(completeRecipes)
    console.log(`Successfully inserted ${result.insertedCount} recipes into the database`)

    // Create indexes for better performance
    await db.collection("recipes").createIndex({ ingredients: 1 })
    await db.collection("recipes").createIndex({ dietaryRestrictions: 1 })
    await db.collection("recipes").createIndex({ cuisine: 1 })
    await db.collection("recipes").createIndex({ difficulty: 1 })
    await db.collection("recipes").createIndex({ cookingTime: 1 })
    await db.collection("recipes").createIndex({ rating: -1 })

    console.log("Database indexes created successfully")
  } catch (error) {
    console.error("Error seeding database:", error)
  } finally {
    await client.close()
  }
}

seedCompleteDatabase()
